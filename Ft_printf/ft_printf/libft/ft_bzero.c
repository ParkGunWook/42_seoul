#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	idx;

	if (n == 0)
		return ;
	idx = 0;
	while (idx < n)
	{
		*((unsigned char*)(s + idx)) = 0;
		idx++;
	}
	return ;
}
