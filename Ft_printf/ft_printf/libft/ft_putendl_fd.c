#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	size_t	idx;

	if (!s)
		return ;
	idx = 0;
	while (*(s + idx))
	{
		write(fd, s + idx, 1);
		idx++;
	}
	write(fd, "\n", 1);
}
